# Base Convertion Build-In Functions

print('\nPython furnizeaza 3 functii build-in pentru conversii')

print('\nPython build-in pentru conversia binara - bin()')
print('bin(16) ->', bin(16))
print('bin(0o123) ->', bin(0o123))
print('bin(0xFace) ->', bin(0xFace))


print('\nPython build-in pentru conversia octala - oct()')
print('oct(100) ->', oct(100))
print('oct(0b111101) ->', oct(0b111101))
print('oct(0xFace) ->', oct(0xFace))

print('\nPython build-in pentru conversia hexa - hex()')
print('hex(1000) ->', hex(1000))
print('hex(0b1010111) ->', hex(0b1010111))
print('hex(0o123456) ->', hex(0o123456))
