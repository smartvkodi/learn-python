# complex Data Type - 
print('\n\nPython - Tipul de date - complex')
print('z = a + bj')

x = 10 + 20j
print(x, ' -> ', type(x))

print('\n\nPython - functii pe numere complexe')

print('\nPartea reala a lui z = x + yJ')
x = 10.5 + 20.4j
print('x = ',x,' -> x.real = ', x.real)

print('\nPartea imaginara a lui z = x + yJ')
x = 1.5 + 0.4j
print('x = ',x,' -> x.imag = ', x.imag)

x = 10 + 20j
y = 20 + 30j
print('\nOperatii aritmetice cu x =',x ,'si y =',y)
print('x+y',x+y)
print('x*y',x*y)
print('x/y',x/y)
